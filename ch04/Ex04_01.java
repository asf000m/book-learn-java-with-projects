package ch04;

/* We need to determine whether a dinosaur is a carnivore or herbivore. 
Write an if statement that prints whether a dinosaur is a carnivore or herbivore based on a boolean variable. 
This information is critical for feeding and care guidelines. */

public class Ex04_01 {
    public static void main(String[] args) {
        boolean is_carnivore = false;
        if (is_carnivore) {
            System.out.println("The dinosaur is and carnivore.");
        } else {
            System.out.println("The dinosaur is herbivore.");
        }
    }
}
